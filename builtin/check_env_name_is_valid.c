int	check_env_name_is_valid(char *s)
{
	if (*s >= '0' && *s <= '9')
		return (0);
	while (*s)
	{
		if (!((*s >= 'a' && *s <= 'z') || \
			(*s >= 'A' && *s <= 'Z') || \
			(*s >= '0' && *s <= '9') || *s == '_'))
			return (0);
		s++;
	}
	return (1);
}
